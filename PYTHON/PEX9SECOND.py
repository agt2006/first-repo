# Built-in Functions Example
# 1. print() - shows message on screen
print("Hello!")
# 2. len() - counts letters in a word
word = "apple"
print("length of word apple:")
print(len(word))  # Output: 5

# 3. type() - shows the type of value
number = 10
print("type of value of 10:")
print(type(number))  # Output: <class 'int'>

# 4. max() - finds the biggest number
print("Maximum number among 3,7,2:")
print(max(3, 7, 2))  # Output: 7

# 5. min() - finds the smallest number
print("Minimum number among 3,7,2:")
print(min(3, 7, 2))  # Output: 2
