# Very Easy Matrix Addition and Transpose

# Matrices (2x2)
a11, a12 = 1, 2
a21, a22 = 3, 4

b11, b12 = 5, 6
b21, b22 = 7, 8

# Print Matrix A
print("Matrix A:")
print(a11, a12)
print(a21, a22)

# Print Matrix B
print("\nMatrix B:")
print(b11, b12)
print(b21, b22)

# Matrix Addition
c11 = a11 + b11
c12 = a12 + b12
c21 = a21 + b21
c22 = a22 + b22

print("\nMatrix Addition (A + B):")
print(c11, c12)
print(c21, c22)

# Transpose of Matrix A
print("\nTranspose of Matrix A:")
print(a11, a21)
print(a12, a22)
