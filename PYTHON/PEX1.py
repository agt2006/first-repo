a = 5             # int
b = 2.5           # float
c = 1 + 2j        # complex
d = [1, 2, 3,"apple"]     # list
e = (4, 5, 6,"banana")     # tuple
f = {"name": "Alice", "age": 25}     # dictionary
g = True          # bool
h = b"hi"         # bytes
i = "hello"       # string

print("Integer:",a)
print("Float:",b)
print("Complex:",c)
print("List:", d)
print("Tuple:", e)
print("Dictionary:", f)
print("Boolean:",g)
print("Bytes:",h)
print("String:", i)
